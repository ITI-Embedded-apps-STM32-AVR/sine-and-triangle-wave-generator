#ifndef DIO_INTERFACE_H_
#define DIO_INTERFACE_H_

/* pins modes */
#define DIO_PIN_MODE_OUT_PUSH_PULL_2MHZ   0b0010
#define DIO_PIN_MODE_OUT_PUSH_PULL_10MHZ  0b0001
#define DIO_PIN_MODE_OUT_PUSH_PULL_50MHZ  0b0011

#define DIO_PIN_MODE_OUT_OPEN_DRAIN_2MHZ  0b0110
#define DIO_PIN_MODE_OUT_OPEN_DRAIN_10MHZ 0b0101
#define DIO_PIN_MODE_OUT_OPEN_DRAIN_50MHZ 0b0111

#define DIO_PIN_MODE_OUT_AF_PUSH_PULL_2MHZ  0b1010
#define DIO_PIN_MODE_OUT_AF_PUSH_PULL_10MHZ 0b1001
#define DIO_PIN_MODE_OUT_AF_PUSH_PULL_50MHZ 0b1011

#define DIO_PIN_MODE_OUT_AF_OPEN_DRAIN_2MHZ  0b1110
#define DIO_PIN_MODE_OUT_AF_OPEN_DRAIN_10MHZ 0b1101
#define DIO_PIN_MODE_OUT_AF_OPEN_DRAIN_50MHZ 0b1111

#define DIO_PIN_MODE_IN_ANALOG       0b0000
#define DIO_PIN_MODE_IN_FLOATING     0b0100
#define DIO_PIN_MODE_IN_PULL_UP_DOWN 0b1000

void DIO_setPinMode(u8 port, u8 pin, u8 mode);
void DIO_setPinVal(u8 port, u8 pin, u8 val);
u8 DIO_getPinVal(u8 port, u8 pin);

#endif /* DIO_INTERFACE_H_ */
